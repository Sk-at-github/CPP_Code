#include<iostream>
using namespace std;

int main(){
    //Bitwise operator
    int a=3,b=5;
    cout<<"a&b"<<(a&b)<< endl;
cout<<"a|b"<<(a|b)<< endl;
cout<<"a^b"<<(a^b)<< endl;
cout<<"~a"<<(~a)<< endl;

//Right shift 
cout<<"15>>2 "<<(15>>2)<< endl;
cout<<"17>>1 "<<(17>>1)<<endl;


//left Shift
cout<<"8<<2 "<<(8<<2)<<endl;
cout<<"15<<1"<<(15<<1)<<endl;

return 0;


}